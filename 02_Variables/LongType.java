public class LongType {
    
    public static void main(String[] args) {
        
        //add L to end of number
        long globalPopulation = 8_000_000_000L;
        //auto complete with sysout
        System.out.println("The global population is " + globalPopulation + ".");

        long googleSearches = 900_000_000_000L;
        System.out.println("Daily, there are " + googleSearches + " Google searches.");
    }
}
