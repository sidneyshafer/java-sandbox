// class name must match file name
// class must contain all code
public class HelloJava {
    
    //main method is the entry point of the application
    public static void main(String[] args) {
        //println function prints a message to the console
        System.out.println("Hello Java");
    }
}

//Terminal Commands
/*
 * javac 
 *  -- Java Compiler: compiles java code into bytecode
 *  -- example: javac HelloJava.java
 *  -- a HelloJava.class is generated where the bytecode is stored
 
 * java
 *  -- java executes the complied code (the class file created from the javac command)
 *  -- java commands DOES NOT use .java extention
 *  -- example: java HelloJava

 * Press the up arrow key to retrieve previous terminal commands
 * Always compile code first (javac), then run compiled code (java)
 */