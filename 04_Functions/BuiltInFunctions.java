public class BuiltInFunctions {
    public static void main(String[] args) {
        //Built-in Functions
        /*
         * - a function that Java made for you
         * - general to every application
         * ex: println() function
         * ex: Math class contains log() function
         */

         System.out.println("Hello Java");

         double sin = Math.sin(1.2);
         System.out.println("The sin of 1.2 is: " + sin);

         // 2 to the power of 3
         double power = Math.pow(2, 4);
         System.out.println("2 to the power of 4 is: " + power);
    }
}
